# Kasutajad ja kasutuslood

## Kasutajarollid

### 1. Administraator
Administraatoril on süsteemis täielik ligipääs. Tema ülesanded:
- kasutajate lisamine, muutmine ja kustutamine;
- rollide ja ligipääsuõiguste haldamine;
- süsteemi seadistuste haldamine.

### 2. Moderaator
Moderaator vastutab sisu kvaliteedi ja sobivuse eest:
- kontrollib eraõpetajate profiile;
- kinnitab või peidab ebasobiva sisu;
- modereerib arvustusi.

### 3. Eraõpetaja
Eraõpetaja kasutab süsteemi enda teenuste haldamiseks:
- haldab enda profiili;
- lisab aineid, kogemust ja hinna;
- haldab enda tundide ajakava ja broneeringuid;
- näeb tagasisidet.

### 4. Õpilane
Õpilane kasutab süsteemi õppimiseks sobiva õpetaja leidmiseks:
- otsib õpetajat aine või hinna järgi;
- broneerib tunni;
- tühistab vajadusel tunni;
- tasub tunni eest;
- kirjutab arvustuse.

### 5. Kasutajatugi
Kasutajatoe spetsialist aitab probleemide lahendamisel:
- konto probleemid;
- broneeringute küsimused;
- maksetega seotud mured.

## Peamised kasutuslood

### Õpilane
- otsib õpetajat;
- valib sobiva aja;
- broneerib tunni;
- teeb makse;
- jätab arvustuse.

### Eraõpetaja
- haldab profiili;
- haldab aineid ja ajakava;
- kinnitab või tühistab broneeringuid.

### Moderaator
- kontrollib õpetaja profiile;
- kontrollib arvustusi;
- otsustab, kas sisu jääb nähtavaks.

### Kasutajatugi
- näeb vajalikke andmeid toe pakkumiseks;
- aitab lahendada kasutajate probleeme.

### Administraator
- haldab kasutajaid;
- haldab rolle ja õigusi;
- omab täielikku ligipääsu süsteemile.
